void test_zero_length_payload_aes()
{
	uint8_t mac[16];

	uint8_t expected_mac[] = { 0xbb, 0x1d, 0x69, 0x29,
		0xe9, 0x59, 0x37, 0x28,
		0x7f, 0xa3, 0x7d, 0x12,
		0x9b, 0x75, 0x67, 0x46 };

	//Case 1: Message length is zero.
	uint8_t payload[1];

	int32_t result = calculate_mac(aes, &aes_key, payload, 0, mac, sizeof(mac));
	assert(result == RESULT_SUCCESS);

	result = memcmp(mac, expected_mac, sizeof(expected_mac));
	assert(result == 0);

	result = verifyMAC(aes_key.aes128.key, payload, 0, mac, sizeof(mac));
	assert(result == 1);
}

